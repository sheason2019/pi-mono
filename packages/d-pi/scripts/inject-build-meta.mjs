#!/usr/bin/env node
import { execSync } from "node:child_process";
import { writeFileSync, mkdirSync } from "node:fs";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const out = `${__dirname}/../src/dpi-meta.generated.ts`;

function getCommit() {
	try {
		return execSync("git rev-parse --short HEAD", { encoding: "utf-8" }).trim();
	} catch {
		return "unknown";
	}
}

function getBuildTime() {
	return new Date().toISOString();
}

const commit = getCommit();
const buildTime = getBuildTime();
const generated = `// AUTO-GENERATED by scripts/inject-build-meta.mjs — do not edit by hand.
export const DPI_BUILD_COMMIT = ${JSON.stringify(commit)};
export const DPI_BUILD_TIME = ${JSON.stringify(buildTime)};
`;

mkdirSync(dirname(out), { recursive: true });
writeFileSync(out, generated, "utf-8");
process.stderr.write(`[inject-build-meta] wrote ${out} (commit=${commit}, time=${buildTime})\n`);
